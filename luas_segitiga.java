package Marshandaproject1;

public class luas_segitiga {
	
	public static void main(String[] args) {
		/*
		 * luas segitiga = (alas*tinggi)
		 */
		double alas = 6;
		double tinggi = 8;
		
		double luas = (alas * tinggi) /2;
		
		System.out.println("Luas segitiga dengan alas 6 dan tinggi 8 = " + luas);
	}

}
